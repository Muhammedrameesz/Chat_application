const mongoose = require("mongoose");

const signupSchema = new mongoose.Schema(
  {
    userName: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
    },
    password: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);

const Signup = mongoose.model("Users",signupSchema)
module.exports = Signup
